            <div class="problem-content">
                <p><strong>The tested oil sample is found in CRITICAL condition.</strong></p>
                <ol class="problem-list">
                    <li>Silicon content is found high, which indicates dust ingress problem.</li>
                    <li>Increase contamination leads to wearing of internal components like gears, shafts, bearings & washers as result PQ Index, Iron, Chromium and Aluminium content are found high.</li>
                    <li>Low Iron and High PQ Index is a result of large Iron particles and few small particles which indicates fatigue wear.</li>
                    <li>As per limit water content is found very high, i.e. 18101 ppm.</li>
                </ol>
                <p><strong>Note:</strong></p>
                <ol>
                    <li>Due to high water content present in the oil, rest of the tests were not possible.</li>
                    <li>Last sample was found in CRITICAL stage. Hence risk is high running the equipment without corrections.</li>
                    <li>Kindly provide the reason for sending the sample in sample Datasheet.</li>
                </ol>
