        <div class="recommendations">
            <div class="recommendations-header">Recommendation:</div>
            <div class="recommendations-content">
                <ol>
                    <li>It is recommended to change the oil.</li>
                    <li>Please check the seal-ability of floating seal for the source of dust & water ingress.</li>
                    <li>It is strongly recommended to check / inspect the internal components like gears, shafts, bearings & washers very closely with a magnifying glass to understand the type of wear of surfaces, diameters and gear teeth flanks.</li>
                    <li>From high water and Silicon content it seems the machine was used in water/mud/slurry. Hence water and mud might have seeped thru Floating Seal of Travel devices. As per Tata Hitachi recommendations/guidelines if machine used in water/mud/slurry, Floating seal need to be replaced.</li>
                    <li>After corrective action resend the sample after 100 hrs. of operation to understand the wear and contamination trend.</li>
